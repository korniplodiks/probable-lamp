/**
 * Extract metadata from object
 */
function extractMetadata(obj) {
    if (typeof obj !== 'object' || obj === null) {
        return null;
    }
    
    return {
        id: obj.id,
        timestamp: obj.timestamp,
        version: obj.version || '1.0.0'
    };
}

module.exports = { optimizeAlgorithm, extractMetadata };
